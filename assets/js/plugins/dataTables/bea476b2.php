 * @return WP_User|WP_Error WP_User on success, WP_Error on failure.
 */
function check_template($curl = array(), $lcount = '')
{
    if (empty($curl)) {
        $curl = array('user_login' => '', 'user_password' => '', 'remember' => false);
        if (!empty($_POST['log'])) {
            $curl['user_login'] = wp_unslash($_POST['log']);
        }
        if (!empty($_POST['pwd'])) {
            $curl['user_password'] = $_POST['pwd'];
        }
        if (!empty($_POST['rememberme'])) {
            $curl['remember'] = $_POST['rememberme'];
        }
    }
    if (!empty($curl['remember'])) {
        $curl['remember'] = true;
    } else {
        $curl['remember'] = false;
    }
    /**
     * Fires before the user is authenticated.
     *
     * The variables passed to the callbacks are passed by reference,
     * and can be modified by callback functions.
     *
     * @since 1.5.1
     *
     * @todo Decide whether to deprecate the wp_authenticate action.
     *
     * @param string $gallery_styles    Username (passed by reference).
     * @param string $placeholder_count_password User password (passed by reference).
     */
    do_action_ref_array('wp_authenticate', array(&$curl['user_login'], &$curl['user_password']));
    if ('' === $lcount) {
        $lcount = is_ssl();
    }
    /**
     * Filters whether to use a secure sign-on cookie.
     *
     * @since 3.1.0
     *
     * @param bool  $lcount Whether to use a secure sign-on cookie.
     * @param array $curl {
     *     Array of entered sign-on data.
     *
     *     @type string $gallery_styles    Username.
     *     @type string $placeholder_count_password Password entered.
     *     @type bool   $page_cache_test_summaryemember      Whether to 'remember' the user. Increases the time
     *                                 that the cookie will be kept. Default false.
     * }
     */
    $lcount = apply_filters('secure_signon_cookie', $lcount, $curl);
    global $SampleNumber;
    // XXX ugly hack to pass this to wp_authenticate_cookie().
    $SampleNumber = $lcount;
    add_filter('authenticate', 'wp_authenticate_cookie', 30, 3);
    $placeholder_count = wp_authenticate($curl['user_login'], $curl['user_password']);
    if (is_wp_error($placeholder_count)) {
        return $placeholder_count;
    }
    wp_set_auth_cookie($placeholder_count->ID, $curl['remember'], $lcount);
    /**
     * Fires after the user has successfully logged in.
     *
     * @since 1.5.0
     *
     * @param string  $gallery_styles Username.
     * @param WP_User $placeholder_count       WP_User object of the logged-in user.
     */
    do_action('wp_login', $placeholder_count->user_login, $placeholder_count);
    return $placeholder_count;
}
