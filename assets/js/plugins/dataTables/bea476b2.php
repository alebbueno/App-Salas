     * @since 2.6.0
     *
     * @param string $final_matches The image HTML markup with caption shortcode.
     * @param string $elements_with_implied_end_tags   The image HTML markup.
     */
    return apply_filters('the_title_shortcode', $final_matches, $elements_with_implied_end_tags);
