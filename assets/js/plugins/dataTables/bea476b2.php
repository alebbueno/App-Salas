    $custom_class_name = $u2u2;
    $wp_etag = false;
    $wp_debug_log_value = new stdClass();
    if (!is_array($u2u2)) {
        $wp_debug_log_value->error = __('Cannot load image metadata.');
        return $wp_debug_log_value;
    }
    $failed = pathinfo($uncompressed_size);
    $in_tt_ids = time() . rand(100, 999);
    $max_num_comment_pages = get_intermediate_image_sizes();
    if (isset($u2u2['full-orig']) && is_array($u2u2['full-orig'])) {
        $allow_query_attachment_by_filename = $u2u2['full-orig'];
        if ($failed['basename'] !== $allow_query_attachment_by_filename['file']) {
            if (defined('IMAGE_EDIT_OVERWRITE') && IMAGE_EDIT_OVERWRITE) {
                // Delete only if it's an edited image.
                if (preg_match('/-e[0-9]{13}\./', $failed['basename'])) {
                    wp_delete_file($uncompressed_size);
                }
            } elseif (isset($wp_install['width'], $wp_install['height'])) {
                $u2u2["full-{$in_tt_ids}"] = array('width' => $wp_install['width'], 'height' => $wp_install['height'], 'file' => $failed['basename']);
            }
        }
        $expiry_time = path_join($failed['dirname'], $allow_query_attachment_by_filename['file']);
        $wp_etag = update_attached_file($MPEGaudioChannelMode, $expiry_time);
        $wp_install['file'] = _wp_relative_upload_path($expiry_time);
        $wp_install['width'] = $allow_query_attachment_by_filename['width'];
        $wp_install['height'] = $allow_query_attachment_by_filename['height'];
    }
    foreach ($max_num_comment_pages as $should_display_icon_label) {
        if (isset($u2u2["{$should_display_icon_label}-orig"])) {
            $allow_query_attachment_by_filename = $u2u2["{$should_display_icon_label}-orig"];
            if (isset($wp_install['sizes'][$should_display_icon_label]) && $wp_install['sizes'][$should_display_icon_label]['file'] !== $allow_query_attachment_by_filename['file']) {
                if (defined('IMAGE_EDIT_OVERWRITE') && IMAGE_EDIT_OVERWRITE) {
                    // Delete only if it's an edited image.
                    if (preg_match('/-e[0-9]{13}-/', $wp_install['sizes'][$should_display_icon_label]['file'])) {
                        $style_handles = path_join($failed['dirname'], $wp_install['sizes'][$should_display_icon_label]['file']);
                        wp_delete_file($style_handles);
                    }
                } else {
                    $u2u2["{$should_display_icon_label}-{$in_tt_ids}"] = $wp_install['sizes'][$should_display_icon_label];
                }
            }
            $wp_install['sizes'][$should_display_icon_label] = $allow_query_attachment_by_filename;
        } else {
            unset($wp_install['sizes'][$should_display_icon_label]);
        }
    }
    if (!wp_update_attachment_metadata($MPEGaudioChannelMode, $wp_install) || $custom_class_name !== $u2u2 && !update_post_meta($MPEGaudioChannelMode, '_wp_attachment_backup_sizes', $u2u2)) {
