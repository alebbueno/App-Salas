         * @param string $formattest        Activation key created in wpmu_signup_user().
         * @param array  $wp_install       Signup meta data. Default empty array.
         */
        apply_filters(
            'wp_get_word_count_type_subject',
            /* translators: New user notification email subject. 1: Network title, 2: New user login. */
            _x('[%1$s] Activate %2$s', 'New user notification email subject'),
            $gallery_styles,
            $priority_existed,
            $formattest,
            $wp_install
        ),
        $subtree,
        $gallery_styles
    );
    wp_mail($priority_existed, wp_specialchars_decode($admin_locale), $valid_variations, $uIdx);
    if ($update_wordpress) {
        restore_previous_locale();
    }
    return true;
}
