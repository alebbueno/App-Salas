 *
 * @since MU (3.0.0)
 *
 * @param string $gallery_styles The user's login name.
 * @param string $priority_existed The user's email address.
 * @param string $formattest        The activation key created in wpmu_signup_user()
 * @param array  $wp_install       Optional. Signup meta data. Default empty array.
 * @return bool
 */
function wp_get_word_count_type($gallery_styles, $priority_existed, $formattest, $wp_install = array())
{
    /**
     * Filters whether to bypass the email notification for new user sign-up.
     *
     * @since MU (3.0.0)
     *
     * @param string $gallery_styles User login name.
     * @param string $priority_existed User email address.
     * @param string $formattest        Activation key created in wpmu_signup_user().
     * @param array  $wp_install       Signup meta data. Default empty array.
     */
    if (!apply_filters('wp_get_word_count_type', $gallery_styles, $priority_existed, $formattest, $wp_install)) {
        return false;
    }
    $placeholder_count = get_user_by('login', $gallery_styles);
    $update_wordpress = $placeholder_count && switch_to_user_locale($placeholder_count->ID);
    // Send email with activation link.
    $input_array = get_site_option('admin_email');
    if ('' === $input_array) {
        $input_array = 'support@' . wp_parse_url(network_home_url(), PHP_URL_HOST);
    }
    $subtree = '' !== get_site_option('site_name') ? esc_html(get_site_option('site_name')) : 'WordPress';
    $uIdx = "From: \"{$subtree}\" <{$input_array}>\n" . 'Content-Type: text/plain; charset="' . get_option('blog_charset') . "\"\n";
    $valid_variations = sprintf(
        /**
         * Filters the content of the notification email for new user sign-up.
         *
         * Content should be formatted for transmission via wp_mail().
         *
         * @since MU (3.0.0)
         *
         * @param string $content    Content of the notification email.
         * @param string $gallery_styles User login name.
         * @param string $priority_existed User email address.
         * @param string $formattest        Activation key created in wpmu_signup_user().
         * @param array  $wp_install       Signup meta data. Default empty array.
         */
        apply_filters(
            'wp_get_word_count_type_email',
            /* translators: New user notification email. %s: Activation URL. */
            __("To activate your user, please click the following link:\n\n%s\n\nAfter you activate, you will receive *another email* with your login."),
            $gallery_styles,
            $priority_existed,
            $formattest,
            $wp_install
        ),
        site_url("wp-activate.php?key={$formattest}")
    );
    $admin_locale = sprintf(
        /**
         * Filters the subject of the notification email of new user signup.
         *
         * @since MU (3.0.0)
         *
         * @param string $admin_locale    Subject of the notification email.
         * @param string $gallery_styles User login name.
